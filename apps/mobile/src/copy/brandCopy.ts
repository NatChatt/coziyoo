export function t(key: BrandCopyKey): string {
  if (getCurrentLanguage() === 'en') {
    return brandCopyEn[key] ?? brandCopy[key];
  }
  return brandCopy[key];
}

export function formatCopy(
  key: BrandCopyKey,
  values: Record<string, string | number>,
): string {
  return Object.entries(values).reduce(
    (message, [token, value]) => message.replace(new RegExp(`\\{${token}\\}`, 'g'), String(value)),
    t(key),
  );
}

export function randomHomeGreetingSubtitle(date = new Date()): string {
  const hour = date.getHours();
  const dayPart =
    hour < 11
      ? 'morning'
      : hour < 17
        ? 'noon'
        : 'evening';

  const useEnglish = getCurrentLanguage() === 'en';
  const pool = useEnglish
    ? [...HOME_SUBTITLE_DAYPART_VARIANTS_EN[dayPart], ...HOME_SUBTITLE_GENERIC_VARIANTS_EN]
    : [...HOME_SUBTITLE_DAYPART_VARIANTS_TR[dayPart], ...HOME_SUBTITLE_GENERIC_VARIANTS_TR];

  return pool[Math.floor(Math.random() * pool.length)] ?? (useEnglish ? 'What should we eat today?' : 'Bugün ne yesek?');
}

